
import java.util.Scanner;

public class dois {
       public static void main(String[] args) {
        Scanner ler = new Scanner(System.in);

        System.out.println("Informe o fim:");
        int numero = ler.nextInt();

        // Subtração
        
        for ( int i = numero ;  i >= 0 ; i-- ) {
            System.out.println(i);
        } 

    }

}
